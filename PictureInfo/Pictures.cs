            }
            public PicturesInfo()
            {
                _values = new List<string>();
            }
            public void addValue(String value)
            {
                _values.Add(value);
            }
            public bool isDefined(String stringToCheck)
            {
                foreach (String value in _values)
                {
                    if (value.Equals(stringToCheck))
                    {
                        return true;
                    }
                }
                return false;
            }
