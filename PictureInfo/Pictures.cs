        public bool isDefined(String stringToCheck)
        {
            foreach (PicturesInfo value in pictureInfo)
            {
                if (value.isDefined(stringToCheck))
                {
                    return true;
                }
            }
            return false;
        }
        public PicturesInfo getPictureByName(String pictureName)
        {
            foreach (PicturesInfo value in pictureInfo)
            {
                if (value.isDefined(pictureName))
                {
                    return value;
                }
            }
            return null;
        }
